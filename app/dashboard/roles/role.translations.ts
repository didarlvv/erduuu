export const translate = (key: string, language: string): string => {
  const keys = key.split(".");
  let translation: any =
    roleTranslations[language as keyof typeof roleTranslations];
  for (const k of keys) {
    if (translation[k] === undefined) {
      return key;
    }
    translation = translation[k];
  }
  return translation;
};
